let move_checker = (move) => {
  switch (move) {
    case "X":
      return 1;
    case "Y":
      return 2;
    case "Z":
      return 3;
    case "A":
      return 1;
    case "B":
      return 2;
    case "C":
      return 3;
  }
};
const outcomes = {
  1: {
    1: 3,
    2: 6,
    3: 0,
  },
  2: {
    1: 0,
    2: 3,
    3: 6,
  },
  3: {
    1: 6,
    2: 0,
    3: 3,
  },
};

const sol1_rps_checker = (game) => {
  const [enemy_turn, my_turn] = game.split(" ").map(move_checker);
  return (+outcomes[enemy_turn][my_turn] || 0) + +my_turn;
};

const sol1 = (moves) => {
  console.log("asserting");
  console.assert(
    sol1_rps_checker("A Y") == 8,
    "In the first round, your opponent will choose Rock (A), and you should choose Paper (Y). This ends in a win for you with a score of 8 (2 because you chose Paper + 6 because you won)."
  );
  console.assert(
    sol1_rps_checker("B X") == 1,
    "In the second round, your opponent will choose Paper (B), and you should choose Rock (X). This ends in a loss for you with a score of 1 (1 + 0)."
  );
  console.assert(
    sol1_rps_checker("C Z") == 6,
    "The third round is a draw with both players choosing Scissors, giving you a score of 3 + 3 = 6."
  );

  console.table({
    "result 1": moves
      .split("\n")
      .map(sol1_rps_checker)
      .reduce((a, g) => +a + +g, 0),
  });
};

