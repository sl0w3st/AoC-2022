const outcome_checker = (outcome) => {
  switch (outcome) {
    case "X":
      return 0;
    case "Y":
      return 3;
    case "Z":
      return 6;
  }
};
const my_move_checker = (enemy_move, outcome) => {
  switch (outcome) {
    case "X":
      return enemy_move - 1 || 3;
    case "Y":
      return enemy_move;
    case "Z":
      return (enemy_move % 3) + 1;
  }
};

const sol2_rps_checker = (game) => {
  const [enemy_literal, outcome] = game.split(" ");
  return (
    +outcome_checker(outcome) +
    +my_move_checker(move_checker(enemy_literal), outcome)
  );
};

const sol2 = (moves) => {
  console.log("asserting");
  console.assert(
    sol2_rps_checker("A Y") == 4,
    "In the first round, your opponent will choose Rock (A), and you need the round to end in a draw (Y), so you also choose Rock. This gives you a score of 1 + 3 = 4."
  );
  console.assert(
    sol2_rps_checker("B X") == 1,
    "In the second round, your opponent will choose Paper (B), and you choose Rock so you lose (X) with a score of 1 + 0 = 1."
  );
  console.assert(
    sol2_rps_checker("C Z") == 7,
    "In the third round, you will defeat your opponent's Scissors with Rock for a score of 1 + 6 = 7."
  );

  console.table({
    "result 2": moves
      .split("\n")
      .map(sol2_rps_checker)
      .reduce((a, g) => +a + +g, 0),
  });
};

sol1(moves);
sol2(moves);
