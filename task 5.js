const moves_parser = (steps) => {
  return steps.split("\n").map((step) => {
    const regex_result = /move (\d+) from (\d+) to (\d+)/g.exec(step);
    const [check, amount, from, to] = regex_result;

    if (!check?.length) throw "Stepper regex mismatch";

    return { amount, from, to };
  });
};

const stacks_parser = (stacks_lines) => {
  // overall its a bad idea to do line splitting and iterate over lines, regular for loops without string modifications will do better
  const all_lines = stacks_lines.split("\n");

  const table_lines = all_lines.slice(0, all_lines.length - 1);

  const stacks_table = table_lines.map(
    (line) =>
      line
        .replace(/(...)\s/g, "$1|") //replacing every 4th space with pipe
        .split("|")
        .map((cell) => cell.replace(/[\s\[\]]/g, "")) // clearing spaces and square brackets
  );

  stacks_table.reverse();

  const stacks_transposed = stacks_table.reduce(
    (acc, row) => {
      row.forEach((cell, col_index) => {
        if (cell) acc[col_index].push(cell);
      });
      return acc;
    },
    Array.from(stacks_table[0], (_) => [])
  );

  return stacks_transposed;
};

const test_drawing = `    [D]    
[N] [C]    
[Z] [M] [P]
 1   2   3 

move 1 from 2 to 1
move 3 from 1 to 3
move 2 from 2 to 1
move 1 from 1 to 2`;

const [test_stacks, test_moves] = test_drawing.split("\n\n");

console.assert(
  moves_parser(test_moves).reduce(
    (a, o) => {
      a.amount += +o.amount || 0;
      a.from += +o.from || 0;
      a.to += +o.to || 0;

      return a;
    },
    { amount: 0, from: 0, to: 0 }
  ).amount == 7,
  "Sum of all .amount from moves"
);

console.assert(
  stacks_parser(test_stacks)[1][1] == "C",
  "Item in middle of stacks table is [C]"
);

const crane = (stacks_lines, move_lines) => {
  const moves = moves_parser(move_lines);
  const stacks = stacks_parser(stacks_lines);

  moves.forEach((move) => {
    const { from, to, amount } = move;
    const items = stacks[+from - 1].splice(-amount, amount);
    items.reverse();
    stacks[+to - 1].push(...items);
  });

  return stacks;
};

console.assert(
  crane(test_stacks, test_moves.split("\n").slice(0, 1).join("\n"))[0].join(
    "|"
  ) == "Z|N|D",
  "First stack after first move"
);

console.assert(
  crane(test_stacks, test_moves.split("\n").slice(0, 2).join("\n"))[2].join(
    "|"
  ) == "P|D|N|Z",
  "Last stack after second move"
);
console.assert(
  crane(test_stacks, test_moves.split("\n").slice(0, 3).join("\n"))[0].join(
    "|"
  ) == "C|M",
  "First stack after third move"
);
console.assert(
  crane(test_stacks, test_moves)[1].join("|") == "M",
  "Middle stack after last move"
);

console.assert(
  crane(test_stacks, test_moves)
    .map((stack) => (stack.length && stack[stack.length - 1]) || "")
    .join("") == "CMZ",
  "the top crates are C in stack 1, M in stack 2, and Z in stack 3, so you should combine these together and give the Elves the message CMZ"
);

